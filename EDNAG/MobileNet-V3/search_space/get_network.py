def get_network(operation_matrix: torch.Tensor, num_class: int, verbose: bool = False):
    arch_str_list, _ = matrix_to_arch_str(operation_matrix.unsqueeze(0))
    arch_str = arch_str_list[0]
    net_config = get_network_config(arch_str=arch_str, num_class=num_class, verbose=verbose)
    net = MobileNetV3.build_from_config(net_config)
    return net, arch_str


